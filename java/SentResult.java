
public class SentResult {	  
	private String result_message;
	private String result_code;
	private String text;
	private String status;
	private String accepted_time;
	private String sent_time;
	private String group_id;
	private String message_id;
	private String type;
	private String recipient_number;
	private String total_count;
	private String list_count;
	private String page;
	private String errorString;
	
	
	public String getResult_message() {
		return result_message;
	}
	public void setResult_message(String result_message) {
		this.result_message = result_message;
	}
	public String getResult_code() {
		return result_code;
	}
	public void setResult_code(String result_code) {
		this.result_code = result_code;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAccepted_time() {
		return accepted_time;
	}
	public void setAccepted_time(String accepted_time) {
		this.accepted_time = accepted_time;
	}
	public String getSent_time() {
		return sent_time;
	}
	public void setSent_time(String sent_time) {
		this.sent_time = sent_time;
	}
	public String getGroup_id() {
		return group_id;
	}
	public void setGroup_id(String group_id) {
		this.group_id = group_id;
	}
	public String getMessage_id() {
		return message_id;
	}
	public void setMessage_id(String message_id) {
		this.message_id = message_id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getRecipient_number() {
		return recipient_number;
	}
	public void setRecipient_number(String recipient_number) {
		this.recipient_number = recipient_number;
	}
	public String getTotal_count() {
		return total_count;
	}
	public void setTotal_count(String total_count) {
		this.total_count = total_count;
	}
	public String getList_count() {
		return list_count;
	}
	public void setList_count(String list_count) {
		this.list_count = list_count;
	}
	public String getPage() {
		return page;
	}
	public void setPage(String page) {
		this.page = page;
	}
	public String getErrorString() {
		return errorString;
	}
	public void setErrorString(String errorString) {
		this.errorString = errorString;
	}
	
}
